    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
        String nombre = this.jTextField2.getText();
        String correo = this.jTextField1.getText();
        String contrasena = this.jTextField3.getText();
         
        UsuarioRegistrado usuarioRegistrado = new UsuarioRegistrado(nombre, correo, contrasena);
        
        if (nombre.equals("") || correo.equals("") || contrasena.equals("") ||
                nombre.equals("Completa todos los campos") || correo.equals("Completa todos los campos") ||
                contrasena.equals("Completa todos los campos")){
            
            this.jTextField1.setText("Completa todos los campos");
            this.jTextField2.setText("Completa todos los campos");
            this.jTextField3.setText("Completa todos los campos");
            
        }else{
            
            try {
            usuarioRegistrado.modificarPerfil(nombre, correo, contrasena);
            } catch (IOException ex) {
                Logger.getLogger(PerfilForm.class.getName()).log(Level.SEVERE, null, ex);
            }

            Perfil2Form s = new Perfil2Form();
            s.setVisible(true);
            this.setVisible(false);
            
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

